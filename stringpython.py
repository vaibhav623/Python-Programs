# taking input from console
s=input()

# printing reverse of s 
print(s[::-1])


# slicing of string 
# print first 3 characters
print(s[:3])

# print last 3 characters
print(s[:len(s)-3-1:-1])

# print alternate characters
print(s[::2])

# print alternate characters from back
print(s[::-2])
