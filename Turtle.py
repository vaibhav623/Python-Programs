import turtle

turtle.bgcolor("black")
turtle.pencolor("yellow")
turtle.color("yellow","green")

turtle.color("turtle")
turtle.pensize(2)

j=200
for i in range(0,17,1):
    turtle.forward(j)
    j=j-10
    turtle.left(90)

turtle.done()
